#12. Construa um programa que receba a altura de três pessoas e calcule a média das três alturas.
#Mostre o resultado na tela.

alt1 = float(input("Informe a primeira altura: "))
alt2 = float(input("Informe a segunda altura: "))
alt3 = float(input("Informe a terceira altura: "))

media = (alt1 + alt2 + alt3) / 3

print(media)